mood_input = input("How are you feeling at the moment?")
if mood_input == "happy":
    print("It is great to see you happy!")
elif mood_input == "nervous":
    print("Take a deep breath 3 times.")
elif mood_input == "sad":
    print("Stop being sad and be awesome instead")
elif mood_input == "excited":
    print("That's good news!")
elif mood_input == "relaxed":
    print("That's good to hear")
else:
    print("I don't recognize this mood")
